package board.vo;

public class MusicBoardVO {
	private int num;
	private String music_name;
	private String music_path;
	private String music_image;
	private String genre;
	private String singger;
	private String album;
	private String title;
	private int hit;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getMusic_name() {
		return music_name;
	}
	public void setMusic_name(String music_name) {
		this.music_name = music_name;
	}
	public String getMusic_path() {
		return music_path;
	}
	public void setMusic_path(String music_path) {
		this.music_path = music_path;
	}
	public String getMusic_image() {
		return music_image;
	}
	public void setMusic_image(String music_image) {
		this.music_image = music_image;
	}
	public String getGenre() {
		return genre;
	}
	public void setGenre(String genre) {
		this.genre = genre;
	}
	public String getSingger() {
		return singger;
	}
	public void setSingger(String singger) {
		this.singger = singger;
	}
	public String getAlbum() {
		return album;
	}
	public void setAlbum(String album) {
		this.album = album;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public int getHit() {
		return hit;
	}
	public void setHit(int hit) {
		this.hit = hit;
	}
	
	
}
